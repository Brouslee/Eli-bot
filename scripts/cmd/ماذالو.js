    onStart: async function ({ api, args, event }) {
        const input = args.join(" ");
        const matches = input.match(/([\u0600-\u06FF]+)\s*\+\s*([\u0600-\u06FF]+)/g);

        if (!matches || matches.length === 0) {
            api.sendMessage(`يرجى تقديم أسماء مفصولة "+".\nعلى سبيل المثال: ماذا_لو أنت + ريم`, event.threadID, event.messageID);
            return;
        }

        const randomIndex = Math.floor(Math.random() * prompts.length);
        let msg = prompts[randomIndex];

        for (const match of matches) {
            const [A, B] = match.split(/\s*\+\s*/);
            msg = msg.replace(/{A}/g, A).replace(/{B}/g, B);
        }

        // تحويل النص إلى نقاط مرقمة ▸
        const formattedMsg = msg.split('. ').map(line => `▸ ${line.trim()}`).join('\n');

        api.sendMessage(formattedMsg, event.threadID, event.messageID);
    },
